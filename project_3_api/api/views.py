from django.contrib.auth import get_user_model
from rest_framework import status
from rest_framework.permissions import AllowAny
from rest_framework.decorators import api_view
from rest_framework.response import Response
from rest_framework.views import APIView
from .models import Post, Comment, ApiGroup
from .serializers import  PostSerializer, RegisterUserSerializer, ApiGroupSerializer, UserSerializer, CommentSerializer

User = get_user_model()

@api_view(['GET', 'POST'])
def apigroup_list(request):
    if request.method == 'GET':
        apigroups = ApiGroup.objects.all()
        serializer = ApiGroupSerializer(apigroups, many=True)
        return Response(serializer.data)

    elif request.method == 'POST':
        serializer = ApiGroupSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


@api_view(['GET', 'PUT', 'DELETE'])
def apigroup_detail(request, pk):
    try:
        apigroup = ApiGroup.objects.get(pk=pk)
    except ApiGroup.DoesNotExist:
        return Response(status=status.HTTP_404_NOT_FOUND)

    if request.method == 'GET':
        serializer = ApiGroupSerializer(apigroup)
        return Response(serializer.data)

    elif request.method == 'PUT':
        serializer = ApiGroupSerializer(apigroup, data=request.data, partial=True)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

    elif request.method == 'DELETE':
        apigroup.delete()
        return Response(status=status.HTTP_204_NO_CONTENT)

